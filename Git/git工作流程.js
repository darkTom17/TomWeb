// ! Git工作流程
// @ 1 克隆远程仓库：
// # 使用 git clone 命令克隆远程仓库到本地。

// @ 2 拉取远程主分支的最新代码：
// # 在你的本地主分支上执行 git pull origin main 命令，以确保你的本地主分支与远程主分支保持同步。

// @ 3 切换到自己的分支：
// # 使用 git checkout 命令切换到你自己的开发分支。

// @ 4 开发工作：
// # 在你的开发分支上进行代码开发，使用 git add 和 git commit 命令来提交你的工作进度。

// @ 5 拉取远程主分支的最新代码：
// # 在提交你的代码之前，再次执行 git pull origin main 命令，以确保你的本地主分支与远程主分支保持同步，避免在推送代码时出现冲突。

// @ 6 解决冲突（如果有）：
// # 如果在拉取远程主分支的最新代码时出现冲突，使用 git merge origin/main 命令来合并远程主分支的代码到你的本地主分支，并解决冲突。

// @ 7 提交代码到远程仓库：
// # 使用 git push origin YourBranchName 命令将你的代码推送到远程仓库的你自己的分支。

// @ 8 创建 Push Request：
// # 如果你的工作需要经过团队审查或合并到主分支，可以在远程仓库中创建一个 Push Request（也称为 Pull Request），让其他团队成员进行代码审查和合并操作。